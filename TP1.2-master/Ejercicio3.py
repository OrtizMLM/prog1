# Dado 2 numeros, mostrar la suma, resta, multiplicacion y division de ambos
# Declaramos y pedimos el ingreso por pantalla de los numeros

print(f"Siempre se efectuara el calculo del numero 1 con el numero 2 en ese orden")

n1 = float(input("Ingrese el primer numero: "))
n2 = float(input("Ingrese el segundo numero: "))

suma = n1 + n2
print(f"La suma de los numeros ingresados es: {suma}")

resta = n1 - n2
print(f"La resta de los numeros ingresados es: {resta}")

mult = n1 * n2
print(f"La multiplicacion de los numeros ingresados es: {mult}")

div = n1 / n2
print(f"La division de los numeros ingresados es: {div}")