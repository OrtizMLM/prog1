# Calcular la media (promedio) de 3 numeros ingresados por teclado:

n1 = float(input("Ingrese el primer numero: "))
n2 = float(input("Ingrese el segundo numero: "))
n3 = float(input("Ingrese el tercer numero: "))

prom = (n1 + n2 + n3)/3

print(f"La media o promedio de los numeros ingresados es: {prom}")
