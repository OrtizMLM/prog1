# Ejercicio 19
# Solicitar al usuario que ingrese el día, mes y año de su nacimiento y almacenar cada uno de ellos en una
#  variable numérica (en total, tres variables diferentes). Finalmente, mostrar la fecha
#  en formato dd/mm/aaaa.

dia = int(input("Ingrese el dia de su nacimiento: "))
mes = int(input("Ingrese el mes de su nacimiento: "))
año = int(input("Ingrese el año de su nacimiento: "))

print(f"{dia} / {mes} / {año}")